package jobsheet15;

public class Customer22 {
    public int id;
    public String name;

    public Customer22() {
    }

    public Customer22(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String toString() {
        return " ID: " + this.id + " Nama: " + this.name;
    }
}
